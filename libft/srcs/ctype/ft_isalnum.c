#include <ctypeft.h>

int			ft_isalnum(int c)
{
	if ((c >= 0x30 && c <= 0x39)
			|| (c >= 0x41 && c <= 0x5A)
			|| (c >= 0x61 && c <= 0x7a))
		return (1);
	return (0);
}
