#include <stdlib.h>
#include "argparser/argparser_argument.h"

void				argparser_argument_del(t_argparser_argument *this)
{
	if (!this)
		return ;
	if (this->str_key)
		free(this->str_key);
	if (this->help)
		free(this->help);
	free(this);
}
