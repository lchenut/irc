#include "channel.h"

static bool	find_fn(void *data, void *context)
{
	return (ft_strcmp(((t_user *)data)->nick, context) == 0);
}

void		channel_del_moderate(t_channel *this, char *username)
{
	vector_find_pop(this->moderate, find_fn, username);
}
