#include "client.h"

void			client_disconnect(t_client *this)
{
	FD_CLR(this->sock, &this->active_set);
	close(this->sock);
	this->sock = -1;
	this->connected = false;
	client_print_and_refresh(this, visual_print_bold, "Disconnected");
}
