#include "buffer.h"

void				buffer_realloc(t_buffer *this)
{
	char			*new_buffer;

	new_buffer = ft_strnew(this->total << 1);
	ft_strcpy(new_buffer, this->buffer);
	free(this->buffer);
	this->buffer = new_buffer;
}
